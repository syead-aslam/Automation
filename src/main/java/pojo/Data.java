package pojo;

public class Data {
    private Character Character;
    private CharLocation Location;
    private Episode Episode;
    private Characters Characters;
    public pojo.Character getCharacter() {
        return Character;
    }

    public void setCharacter(pojo.Character character) {
        Character = character;
    }

    public CharLocation getLocation() {
        return Location;
    }

    public void setLocation(CharLocation location) {
        Location = location;
    }

    public Episode getEpisode() {
        return Episode;
    }

    public void setEpisode(Episode episode) {
        Episode = episode;
    }

    public Characters getCharacters() {
        return Characters;
    }

    public void setCharacters(Characters characters) {
        Characters = characters;
    }

}
